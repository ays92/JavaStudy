package variable_변수.ex;

public class VarEx3 {
    public static void main(String[] args) {
        long lon = 10000000000L;
        System.out.println(lon);
        System.out.println("lon = " + lon); // soutv 라는 단축키를 사용하면 가까이 위치한 변수의 값을 가져와서 출력해줌

        boolean bool = true;
        System.out.println(bool);
    }
}
